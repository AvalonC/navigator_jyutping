<p class="hint-container-title">關於粵語中“入”聲調在粵拼中的使用</p>
<p>已經掌握粵語的讀者會發現此處未有標注高陰入，低陰入和陽入。</p>
<p>實際上來説，高陰入、低陰入、陽入聲調的音高，與陰平、陰去、陽去是一樣的（55 &amp; 5, 33 &amp;3, 22 &amp;2），在粵拼方案中，以-p、-t、-k這些韻尾做出區別，傳統聲調的定義，包括抑揚性（即實際音高）和頓挫性。而入聲韻尾 -p、-t、-k 正是影響了其頓挫性。</p>
<p>因此粵語有「九聲六調」，標調時可以只視乎實際音高，用1至6六個數字，入聲看韻尾字母區分即可。懂粵語的讀者可將下方例和上方例一同閲讀下來即可理解。</p>
